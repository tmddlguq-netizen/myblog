export const getRandomGradient = (seed: string): string => {
  // 문자열을 해시하여 일관된 인덱스 생성
  let hash = 0;
  for (let i = 0; i < seed.length; i++) {
    hash = seed.charCodeAt(i) + ((hash << 5) - hash);
  }
  const index = Math.abs(hash) % gradients.length;
  return gradients[index];
};
